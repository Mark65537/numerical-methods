#include <iostream>
#include <cmath>

using namespace std;

/**
 * @brief Вычисляет значение исходной функции f(x).
 *
 * Функция задаёт уравнение:
 *      f(x) = sin(x^2) - 6x + 1
 *
 * Используется для поиска корня уравнения f(x) = 0.
 *
 * @param x Входное значение аргумента.
 * @return Значение функции в точке x.
 */
float f(float x)
{
    return sin(x * x) - 6 * x + 1;
}

/**
 * @brief Итерационная функция φ(x) для метода простой итерации.
 *
 * Получена преобразованием исходного уравнения f(x) = 0 к виду:
 *      x = (sin(x^2) + 1) / 6
 *
 * Используется в методе простой итерации:
 *      x_{k+1} = φ(x_k)
 *
 * @param x Текущее приближение.
 * @return Следующее приближение.
 */
float phi(float x)
{
    return (sin(x * x) + 1) / 6;
}

/**
 * @brief Производная (численно, через конечные разности)
 *
 *
 * @param x
 * @return
 */
float derivative(float x)
{
    const float h = 1e-5f;
    return (f(x + h) - f(x)) / h;
}

/**
 * @brief Метод Ньютона (метод касательных) для нахождения корня уравнения.
 *
 * Реализует итерационный процесс:
 *      x_{k+1} = x_k - f(x_k) / f'(x_k)
 *
 * Производная вычисляется численно методом конечных разностей.
 *
 * ВНИМАНИЕ:
 * - Производная вычисляется только один раз (что снижает точность).
 * - Условие выхода содержит ошибку (должно быть > eps).
 *
 * @param x0 Начальное приближение.
 * @param x Текущее значение (обычно начальное значение).
 * @param eps Точность (критерий остановки).
 */
float newton(float x0, float eps)
{
    float x;
    do
    {
        x = x0;
        x0 = x - f(x) / derivative(x); // пересчёт производной на каждом шаге
    } while (abs(x0 - x) > eps);

    return x0;
}

/**
 * @brief Метод простой итерации для нахождения корня уравнения.
 *
 * Реализует итерационный процесс:
 *      x_{k+1} = φ(x_k)
 *
 * где φ(x) = (sin(x^2) + 1) / 6.
 *
 * Сходимость метода зависит от выбора функции φ(x)
 * и начального приближения.
 *
 * @param x0 Начальное приближение (используется внутри итераций).
 * @param x Текущее значение (начальное значение).
 * @param eps Точность (критерий остановки).
 */
float simpleIteration(float x0, float eps)
{
    float x;
    do
    {
        x = x0;
        x0 = phi(x);
    } while (abs(x0 - x) > eps);

    return x0;
}

/**
 * @brief Точка входа в программу.
 *
 * Инициализирует:
 * - начальное приближение x0
 * - текущее значение x
 * - точность eps
 *
 * Запускает:
 * - метод Ньютона
 * - метод простой итерации
 *
 * Выводит найденные корни в консоль.
 */
int main()
{
    float x0, eps;
    x0 = 0.1;
    eps = 0.000001;

    cout << "Newton: " << newton(x0, eps) << endl;
    cout << "Iteration: " << simpleIteration(x0, eps) << endl;

    return 0;
}